package com.example.xuversion.class_add_activity;

public class LectureTime {
    private String dajie;
    private String time;

    /**
     * Dajie getter
     * @return the dajie name
     */
    public String getDajie() {
        return dajie;
    }

    /**
     * Dajie setter
     * @param dajie the name that you want to set
     */
    public void setDajie(String dajie) {
        this.dajie = dajie;
    }

    /**
     * Time getter
     * @return the time
     */
    public String getTime() {
        return time;
    }

    /**
     * Time setter
     * @param time the time you want to set
     */
    public void setTime(String time) {
        this.time = time;
    }
}
